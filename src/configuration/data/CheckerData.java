package configuration.data;

import java.util.ArrayList;

/**
 *
 * @author Tecnovision
 */
public class CheckerData {
    
    private final String ok;
    private final String ko;
    private final ArrayList<String> oidList;

    public CheckerData(String ok, String ko, ArrayList<String> oidList) {
        this.ok = ok;
        this.ko = ko;
        this.oidList = (oidList != null) ? oidList : new ArrayList<String>();
    }

    public String getOk() {
        return ok;
    }

    public String getKo() {
        return ko;
    }

    public ArrayList<String> getOidList() {
        return oidList;
    }
}
